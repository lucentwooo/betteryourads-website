export interface Creative { src: string; alt: string; }
export interface BrandSet { brand: string; label: string; ads: Creative[]; }

/** Build a brand's ad list. Pass a count for sequential ad-01..0N, or an
 *  explicit list of indices (e.g. to skip a weak creative). `kind`
 *  controls the alt: the wall holds REAL reference ads from these brands
 *  (the library the model trains on), not our generated output. */
const ads = (
  brand: string,
  nOrIdx: number | number[],
  label: string,
  kind: 'generated' | 'reference' = 'reference',
): Creative[] => {
  const idx = Array.isArray(nOrIdx) ? nOrIdx : Array.from({ length: nOrIdx }, (_, i) => i + 1);
  return idx.map((i) => ({
    src: `/creatives/${brand}/ad-0${i}.jpg`,
    alt:
      kind === 'generated'
        ? `${label} ad creative · generated by Loopy`
        : `${label} ad · Loopy reference library`,
  }));
};

